/**
 * BackSpace function is similar to the above ones.
 * First, we are getting the overall length of the text that was supplied and storing it in variable called temA
 * Secondly, since backspace removes 1 letter each time, we set temB = 1
 * We then, returned the lenght and stored it in variable called temC
 * Once we have got the lenght, it was time to output 
 * .slice returns or displays everything that's between the array index of 0 to the value of temC
 */

/**
 * For example, let's say the text entered was "Aakash" and wanted to apply BackSpace
 * 1) temA's length = 6
 * 2) temB = 1
 * 3) temC = temA - temB = 6 - 1 == 5
 * 4) .slice(0, temC) => .slice(0,5) = "Aakash"
 */

function funBackSpace(){
	var letter = document.getElementById('text').innerHTML;
	var temA = document.getElementById('text').innerHTML.length;
	var img= document.getElementById("Image");
	var temB = 1;
	var temC = temA - temB;
	document.getElementById('text').innerHTML=letter.slice(0,temC);
	img.removeChild(img.childNodes[temC])
	
} 

